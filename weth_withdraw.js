// Fungsi untuk melakukan transaksi withdraw
async function withdraw() {
  const amount = ethers.utils.parseEther("0.05"); // Jumlah withdraw, contoh: 0.1 WETH

  try {
    const tx = await contract.withdraw(amount, {
      gasPrice: ethers.utils.parseUnits("0.095", "gwei"),
      gasLimit: 100000,
    });

    console.log("Transaction Hash:", tx.hash);

    const receipt = await tx.wait();
    console.log("Transaction was mined in block:", receipt.blockNumber);
  } catch (error) {
    console.error("Transaction failed:", error);
  }
}

// Memanggil fungsi withdraw
withdraw().catch(console.error);
