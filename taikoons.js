// Initialize contract
const nftContract = new ethers.Contract(contractAddress, contractABI, provider);

const getNFTHolders = async () => {
  try {
    const holders = new Set();

    // Iterate through each token ID to get the owner
    for (let tokenId = 1; tokenId <= 500; tokenId++) {
      try {
        const owner = await nftContract.ownerOf(tokenId);
        holders.add(owner);
        console.log(`Token ID ${tokenId}: Owner ${owner}`);
      } catch (error) {
        console.error(`Error fetching owner for token ID ${tokenId}:`, error);
      }
    }

    // Convert set to array and save to JSON file
    const holdersArray = Array.from(holders);
    const jsonData = JSON.stringify(holdersArray, null, 2);
    fs.writeFileSync("taikoon.json", jsonData);

    console.log("Successfully saved NFT holders to taikoon.json");
  } catch (error) {
    console.error("Error reading NFT contract data:", error);
  }
};

getNFTHolders();
